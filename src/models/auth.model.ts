import mongoose from "mongoose";
const { Schema } = mongoose;

const authSchema = new Schema({
  birthday: {
    type: String,
  },
  gen: {
    type: String,
  },
  first_name: {
    type: String,
    required: true,
  },
  last_name: {
    type: String,
    required: true,
  },
  image: {
    type: String,
  },
  username: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  createAt: {
    type: Date,
    default: Date.now,
  },
});

const auth = mongoose.model("auth", authSchema);

export default auth;
